# ---  Class ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£ Key Rotation ---
class GeminiKeyManager:
    def __init__(self):
        self.keys = []
        self.current_index = 0
        
        # ‡πÇ‡∏´‡∏•‡∏î Key ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏ó‡∏µ‡πà‡∏°‡∏µ‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö GEMINI_API_KEY_1, _2, _3 ...
        i = 1
        while True:
            key = os.getenv(f"GEMINI_API_KEY_{i}")
            if key:
                self.keys.append(key)
                i += 1
            else:
                break
        
        # ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ‡πÅ‡∏ö‡∏ö‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç ‡πÉ‡∏´‡πâ‡∏•‡∏≠‡∏á‡∏´‡∏≤‡πÅ‡∏ö‡∏ö‡πÄ‡∏î‡∏µ‡πà‡∏¢‡∏ß‡πÜ ‡πÄ‡∏î‡∏¥‡∏° (GEMINI_API_KEY)
        if not self.keys and os.getenv("GEMINI_API_KEY_1"):
            self.keys.append(os.getenv("GEMINI_API_KEY_1"))
            
        if not self.keys:
            logger.critical("!!! CRITICAL ERROR: No GEMINI_API_KEY found. Exiting. !!!")
            exit(1)
            
        logger.info(f"Loaded {len(self.keys)} Gemini API Keys.")
        self._configure_current_key()

    def _configure_current_key(self):
        """‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ GenAI ‡∏î‡πâ‡∏ß‡∏¢ Key ‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô"""
        current_key = self.keys[self.current_index]
        genai.configure(api_key=current_key)
        # ‚≠êÔ∏è ‡πÉ‡∏ä‡πâ‡∏£‡∏∏‡πà‡∏ô 1.5 Flash (‡πÄ‡∏™‡∏ñ‡∏µ‡∏¢‡∏£‡∏™‡∏∏‡∏î)
        self.model = genai.GenerativeModel('gemini-2.5-flash-lite') 
        logger.info(f"Switched to Gemini Key Index: {self.current_index + 1}/{len(self.keys)}")

    def rotate_key(self):
        """‡∏™‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡πÉ‡∏ä‡πâ Key ‡∏ñ‡∏±‡∏î‡πÑ‡∏õ"""
        self.current_index = (self.current_index + 1) % len(self.keys)
        self._configure_current_key()

    def get_model(self):
        return self.model

